// `os.Exit` выкарыстоўваецца для неадкладнага выканання праграмы з зададзеным
// статусам.

package main

import (
	"fmt"
	"os"
)

func main() {

	// `defer`s _не_ будуць выконвацца пры выкарыстанні `os.Exit`, таму
	// гэты `fmt.Println` ніколі не будзе выкліканы.
	defer fmt.Println("!")

	// Выхад з статусам 3.
	os.Exit(3)
}

// Звярніце ўвагу, у адрозненне ад C, функцыя `main` в Go не павяртае ніякіх
// значэнняў. Калі трэба перадаць не нулявы статус каманды
// выкарыстоўваце `os.Exit`.
