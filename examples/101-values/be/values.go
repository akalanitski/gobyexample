// Go мае розныя тыпы значэнняў, у тым ліку тэкст,
// цэлыя лікі, дробныя лікі (лікі з плаваючай кропкай), булеўскія і г.д.
// Некалькі асноўных прыкладаў.

package main

import "fmt"

func main() {

	// тэкст, якія можна склеіваць з дапамогай `+`.
	fmt.Println("go" + "lang")

	// Цэлыя лікі і дробныя лікі.
	fmt.Println("1+1 =", 1+1)
	fmt.Println("7.0/3.0 =", 7.0/3.0)

	// Булевы значэнні, з булевымі аператарамі.
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
}
