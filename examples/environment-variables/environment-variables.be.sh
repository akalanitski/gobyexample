# Запуск праграмы паказвае, што мы атрымліваем значэнне
# для `FOO`, якое мы ўстанавілі ў праграме, але
# `BAR` пусты.
$ go run environment-variables.go
FOO: 1
BAR: 

# Спіс ключоў у асяроддзі будзе залежаць ад вашай
# канкрэтнай машыны.
TERM_PROGRAM
PATH
SHELL
...
FOO

# Калі мы спачатку ўсталюем `BAR` у асяроддзі, запушчаная праграма
# возьме гэтае значэнне.
$ BAR=2 go run environment-variables.go
FOO: 1
BAR: 2
...
